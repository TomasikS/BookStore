package com.BookApp.model;

import java.util.Date;

public class Vypozicka {
private int id;
private Date von;
private Date bis;
private int bookid;


public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public Date getVon() {
	return von;
}
public void setVon(Date von) {
	this.von = von;
}
public Date getBis() {
	return bis;
}
public void setBis(Date bis) {
	this.bis = bis;
}
public int getBookid() {
	return bookid;
}
public void setBookid(int bookid) {
	this.bookid = bookid;
}





}
